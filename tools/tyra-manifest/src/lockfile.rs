#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn roundtrip_empty() {
        let lf = Lockfile {
            version: 1,
            packages: vec![],
        };
        let s = toml::to_string_pretty(&lf).unwrap();
        let lf2: Lockfile = toml::from_str(&s).unwrap();
        assert_eq!(lf2.version, 1);
        assert!(lf2.packages.is_empty());
    }

    #[test]
    fn roundtrip_git_entry() {
        let lf = Lockfile {
            version: 1,
            packages: vec![LockedPackage {
                name: "foo".to_string(),
                source: "git+https://github.com/user/foo.git".to_string(),
                rev: Some("abc1234".to_string()),
                branch: None,
                pkg_version: Some("1.0.0".to_string()),
            }],
        };
        let s = toml::to_string_pretty(&lf).unwrap();
        let lf2: Lockfile = toml::from_str(&s).unwrap();
        assert_eq!(lf2.packages[0].rev.as_deref(), Some("abc1234"));
        assert_eq!(lf2.packages[0].pkg_version.as_deref(), Some("1.0.0"));
    }

    #[test]
    fn roundtrip_path_entry() {
        let lf = Lockfile {
            version: 1,
            packages: vec![LockedPackage {
                name: "bar".to_string(),
                source: "path+../bar".to_string(),
                rev: None,
                branch: None,
                pkg_version: None,
            }],
        };
        let s = toml::to_string_pretty(&lf).unwrap();
        assert!(!s.contains("rev ="));
        assert!(!s.contains("branch ="));
        assert!(!s.contains("pkg_version ="));
    }

    #[test]
    fn roundtrip_branch_entry() {
        let lf = Lockfile {
            version: 1,
            packages: vec![LockedPackage {
                name: "baz".to_string(),
                source: "git+https://github.com/user/baz.git".to_string(),
                rev: Some("deadbeef".to_string()),
                branch: Some("main".to_string()),
                pkg_version: None,
            }],
        };
        let s = toml::to_string_pretty(&lf).unwrap();
        assert!(s.contains("branch = \"main\""));
        let lf2: Lockfile = toml::from_str(&s).unwrap();
        assert_eq!(lf2.packages[0].branch.as_deref(), Some("main"));
        assert_eq!(lf2.packages[0].rev.as_deref(), Some("deadbeef"));
    }
}
